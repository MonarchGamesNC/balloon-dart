# Install emsdk

# Curl download emsdk from this github url https://github.com/emscripten-core/emsdk/archive/refs/tags/3.1.60.tar.gz
curl -LO https://github.com/emscripten-core/emsdk/archive/refs/tags/3.1.60.tar.gz -o emsdk.tar.gz

# untar the downloaded file emsdk.tar.gz
tar -xvf emsdk.tar.gz

